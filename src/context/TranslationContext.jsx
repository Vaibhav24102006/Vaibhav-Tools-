export const TranslationProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('hi'); // Default to Hindi
  const [isTranslating, setIsTranslating] = useState(false);

  // Function to translate text
  const translate = (text) => {
    if (!text) return '';
    
    // If we have a direct translation, use it
    if (translations[currentLanguage] && translations[currentLanguage][text]) {
      return translations[currentLanguage][text];
    }
    
    // If no translation found, return original text
    return text;
  };

  // Function to toggle language
  const toggleLanguage = () => {
    setIsTranslating(true);
    
    // Small delay for visual feedback
    setTimeout(() => {
      setCurrentLanguage(prev => prev === 'en' ? 'hi' : 'en');
      setIsTranslating(false);
    }, 200);
  };

  // Function to set specific language
  const setLanguage = (lang) => {
    if (lang !== currentLanguage) {
      setIsTranslating(true);
      setTimeout(() => {
        setCurrentLanguage(lang);
        setIsTranslating(false);
      }, 200);
    }
  };

  const value = {
    currentLanguage,
    translate,
    toggleLanguage,
    setLanguage,
    isTranslating,
    isHindi: currentLanguage === 'hi',
    isEnglish: currentLanguage === 'en'
  };

  return (
    <TranslationContext.Provider value={value}>
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error('useTranslation must be used within a TranslationProvider');
  }
  return context;
};

export default TranslationContext;
